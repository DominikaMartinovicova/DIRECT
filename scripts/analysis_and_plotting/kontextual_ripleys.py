# 4  Main analysis loop
#==============================================================================
results = {}

print(f"Running kontextual Ripley's L for {len(parent_comb)} pairs "
      f"(n_sim={args.n_sim}, seed={args.seed})\n")

for _, row in tqdm(parent_comb.iterrows(), total=len(parent_comb), desc="Pairs"):
    type_i      = row['from']
    type_j      = row['to']
    parent_name = row['parent_name']
    parent_list = row['parent_list']

    # Permutation pool = parent types ∩ present only.
    # type_j is NOT added: if it falls outside the parent compartment its
    # positions are fixed as the target across observed + all simulations.
    perm_types = set(parent_list) & present_types

    pool = get_pool(perm_types)
    if pool is None:
        results[(type_i, type_j)] = empty_result(
            type_i, type_j, parent_name, 'pool_too_small')
        continue

    perm_labels         = pool['labels']
    perm_coords         = pool['coords']
    n_parent_at_pool    = pool['n_parent_at_pool']   # (n_pool, N_R)
    pool_gw             = pool['pool_global_weights'] # (n_pool, N_R)
    n_pool              = len(perm_coords)

    mask_i = perm_labels == type_i
    n_i    = int(np.sum(mask_i))

    # -----------------------------------------------------------------------
    # Target cells: fixed if type_j is outside the parent pool
    # -----------------------------------------------------------------------
    type_j_in_pool = type_j in perm_types
    if type_j_in_pool:
        # All type_j cells are within the pool; their labels are permuted.
        mask_j         = perm_labels == type_j
        n_j            = int(np.sum(mask_j))
        coords_j_fixed = None        # resolved from pool per permutation
        n_par_tgt_fixed = None
    else:
        # type_j is outside the parent compartment — positions are invariant.
        mask_j_global   = labels == type_j
        coords_j_fixed  = coordinates[mask_j_global]
        n_j             = int(np.sum(mask_j_global))
        # Parent counts at fixed target positions (precomputed once; pool
        # positions never change, only labels within pool are shuffled).
        n_par_tgt_fixed = precompute_parent_counts(
                              coords_j_fixed, perm_coords, RADII)

    if n_i < args.min_cells:
        res = empty_result(type_i, type_j, parent_name, 'insufficient_source_cells')
        res['n_source_cells'] = n_i
        res['n_target_cells'] = n_j
        results[(type_i, type_j)] = res
        continue
    if n_j < args.min_cells:
        res = empty_result(type_i, type_j, parent_name, 'insufficient_target_cells')
        res['n_source_cells'] = n_i
        res['n_target_cells'] = n_j
        results[(type_i, type_j)] = res
        continue

    # -----------------------------------------------------------------------
    # Observed L_original and L_kontextual
    # -----------------------------------------------------------------------
    coords_i        = perm_coords[mask_i]
    weights_i       = pool_gw[mask_i]
    n_parent_at_src = n_parent_at_pool[mask_i]   # (n_i, N_R)

    if type_j_in_pool:
        coords_j      = perm_coords[mask_j]
        n_par_tgt_obs = n_parent_at_pool[mask_j]  # (n_j, N_R)
    else:
        coords_j      = coords_j_fixed
        n_par_tgt_obs = n_par_tgt_fixed

    L_obs_orig = cross_L_original(
        coords_i, coords_j, RADII, weights_i, global_area)

    L_obs_kont = cross_L_kontextual(
        coords_i, coords_j,
        n_parent_at_src, n_par_tgt_obs,
        n_pool, n_j, weights_i, RADII)

    # -----------------------------------------------------------------------
    # Permutation nulls (one draw per statistic, per simulation):
    #
    # L_original null — global label permutation:
    #   All cell labels are reshuffled across the entire tissue.  Tests
    #   whether type_i clusters near type_j more than expected under
    #   complete spatial randomness of cell identity.  This matches
    #   R Statial's relabelCells behaviour and avoids the degenerate
    #   trivial null that arises when the parent pool contains only type_i.
    #
    # L_kontextual null — parent-pool label permutation:
    #   Labels are shuffled only within the parent compartment.  Tests
    #   whether the observed enrichment exceeds what is expected if type_i
    #   were randomly distributed within its parent population.
    #   - type_j INSIDE pool : target coords + parent counts change each draw.
    #   - type_j OUTSIDE pool: target coords + parent counts are invariant;
    #     only source assignment changes.
    #   Pool cell positions never move → n_parent_at_pool is reused as-is.
    # -----------------------------------------------------------------------
    sims_orig = []
    sims_kont = []
    n_failed  = 0

    for _ in range(args.n_sim):
        # --- L_original null: permute ALL labels globally ----------------
        # Counts are preserved across permutation so min-cells never fails.
        perm_global  = rng.permutation(labels)
        mask_i_g     = perm_global == type_i
        mask_j_g     = perm_global == type_j
        ci_g         = coordinates[mask_i_g]
        wi_g         = global_weights[mask_i_g]
        cj_g         = coordinates[mask_j_g]

        Lo = cross_L_original(ci_g, cj_g, RADII, wi_g, global_area)
        sims_orig.append(Lo)

        # --- L_kontextual null: permute labels within parent pool only ---
        perm_l   = rng.permutation(perm_labels)
        mask_i_p = perm_l == type_i
        ci = perm_coords[mask_i_p]
        wi = pool_gw[mask_i_p]

        if type_j_in_pool:
            mask_j_p    = perm_l == type_j
            nj_p        = int(np.sum(mask_j_p))
            cj          = perm_coords[mask_j_p]
            n_par_tgt_p = n_parent_at_pool[mask_j_p]
        else:
            cj          = coords_j_fixed
            nj_p        = n_j
            n_par_tgt_p = n_par_tgt_fixed

        Lk = cross_L_kontextual(
                 ci, cj,
                 n_parent_at_pool[mask_i_p],
                 n_par_tgt_p,
                 n_pool, nj_p, wi, RADII)
        sims_kont.append(Lk)

    n_valid = len(sims_orig)

    if n_valid == 0:
        res = empty_result(type_i, type_j, parent_name, 'no_valid_simulations')
        res['n_source_cells'] = n_i
        res['n_target_cells'] = n_j
        results[(type_i, type_j)] = res
        continue

    sims_orig = np.array(sims_orig)   # (n_valid, N_R)
    sims_kont = np.array(sims_kont)   # (n_valid, N_R)

    # -----------------------------------------------------------------------
    # Statistics for original and kontextual
    # -----------------------------------------------------------------------
    stats_orig = _integral_stats(L_obs_orig, sims_orig, RADII)
    stats_kont = _integral_stats(L_obs_kont, sims_kont, RADII)

    results[(type_i, type_j)] = {
        "type_i":      type_i,
        "type_j":      type_j,
        "parent_name": parent_name,
        "perm_pool_types": sorted(perm_types),
        "r":           RADII.copy(),
        # ---- original (homogeneous, global window) = R "original" ----
        "L_original":              L_obs_orig,
        "L_original_sim_mean":     stats_orig['sim_mean'],
        "L_original_sim_std":      stats_orig['sim_std'],
        "L_original_simulations":  sims_orig,
        "pvalues_original":        stats_orig['pvalues'],
        "integral_original":       stats_orig['integral_signed'],
        "integral_original_abs":   stats_orig['integral_abs'],
        "z_original":              stats_orig['z_integral'],
        "p_original":              stats_orig['p_integral'],
        # ---- kontextual (inhomogeneous parent, global window) = R "kontextual" ----
        "L_kontextual":             L_obs_kont,
        "L_kontextual_sim_mean":    stats_kont['sim_mean'],
        "L_kontextual_sim_std":     stats_kont['sim_std'],
        "L_kontextual_simulations": sims_kont,
        "pvalues_kontextual":       stats_kont['pvalues'],
        "integral_kontextual":      stats_kont['integral_signed'],
        "integral_kontextual_abs":  stats_kont['integral_abs'],
        "z_kontextual":             stats_kont['z_integral'],
        "p_kontextual":             stats_kont['p_integral'],
        # ---- counts / diagnostics ----
        "n_source_cells":       n_i,
        "n_target_cells":       n_j,
        "n_valid_simulations":  n_valid,
        "n_failed_simulations": n_failed,
        "reason":               None,
    }

print(f"\nFinished cross-type pairs: "
      f"{sum(1 for v in results.values() if v['reason'] is None)} computed, "
      f"{sum(1 for v in results.values() if v['reason'] is not None)} skipped.")

#==============================================================================
# 4b  Diagonal: same-type self-clustering  (L_original + L_kontextual)
#==============================================================================
# L_original null : global label permutation (same as cross-type).
# L_kontextual    : uses the same parent compartment as defined for ct-as-source
#                   in parent_comb.  If the pool contains only ct (e.g. Tumor_cells
#                   whose parent = self), the kontextual null is degenerate and is
#                   stored as NaN — the original curve still carries full information.

print(f"\nComputing diagonal (self-clustering) for {len(args.coi)} COI types...")

# Build lookup: ct → (parent_list, parent_name) from the first matching row
_ct_parent = {}
for ct in args.coi:
    rows = parent_comb[parent_comb['from'] == ct]
    if len(rows) > 0:
        _ct_parent[ct] = (rows.iloc[0]['parent_list'], rows.iloc[0]['parent_name'])
    else:
        _ct_parent[ct] = ([ct], 'self')

for ct in args.coi:
    if ct not in present_types:
        print(f"  [{ct}] not present — skipping")
        continue

    mask_ct  = labels == ct
    n_ct     = int(np.sum(mask_ct))

    if n_ct < args.min_cells:
        res = empty_result(ct, ct, 'self', 'insufficient_source_cells')
        res['n_source_cells'] = n_ct
        res['n_target_cells'] = n_ct
        results[(ct, ct)] = res
        print(f"  [{ct}] only {n_ct} cells — skipped")
        continue

    coords_ct  = coordinates[mask_ct]
    weights_ct = global_weights[mask_ct]

    # ------------------------------------------------------------------
    # Observed L_original (self-clustering, global window)
    # ------------------------------------------------------------------
    L_obs_orig = cross_L_original(
        coords_ct, coords_ct, RADII, weights_ct, global_area)

    # ------------------------------------------------------------------
    # Observed L_kontextual (self-clustering, parent-density weighted)
    # ------------------------------------------------------------------
    parent_list_ct, parent_name_ct = _ct_parent[ct]
    perm_types_ct = set(parent_list_ct) & present_types
    pool_ct       = get_pool(perm_types_ct)

    nan_r = np.full(N_R, np.nan)

    if pool_ct is not None:
        perm_labels_ct   = pool_ct['labels']
        perm_coords_ct   = pool_ct['coords']
        n_par_at_pool_ct = pool_ct['n_parent_at_pool']
        pool_gw_ct       = pool_ct['pool_global_weights']
        n_pool_ct        = len(perm_coords_ct)

        mask_src = perm_labels_ct == ct
        n_src    = int(np.sum(mask_src))

        if n_src >= args.min_cells:
            ci_ct        = perm_coords_ct[mask_src]
            wi_ct        = pool_gw_ct[mask_src]
            n_par_src_ct = n_par_at_pool_ct[mask_src]
            # source == target for self-pair
            L_obs_kont = cross_L_kontextual(
                ci_ct, ci_ct,
                n_par_src_ct, n_par_src_ct,
                n_pool_ct, n_src, wi_ct, RADII)
        else:
            L_obs_kont = nan_r.copy()
            pool_ct    = None          # flag: skip kontextual null below
    else:
        L_obs_kont = nan_r.copy()

    # ------------------------------------------------------------------
    # Simulation loops
    # ------------------------------------------------------------------
    sims_orig = []
    sims_kont = []

    for _ in range(args.n_sim):
        # L_original null: global permutation
        perm_global = rng.permutation(labels)
        mask_ct_g   = perm_global == ct
        ci_g        = coordinates[mask_ct_g]
        wi_g        = global_weights[mask_ct_g]
        sims_orig.append(cross_L_original(ci_g, ci_g, RADII, wi_g, global_area))

        # L_kontextual null: pool-restricted permutation
        # Degenerate when pool contains only ct (all draws identical) — stored
        # but downstream stats will show z = NaN / p = NaN via zero variance.
        if pool_ct is not None and n_src >= args.min_cells:
            perm_l_ct   = rng.permutation(perm_labels_ct)
            mask_i_p    = perm_l_ct == ct
            ci_p        = perm_coords_ct[mask_i_p]
            wi_p        = pool_gw_ct[mask_i_p]
            n_par_p     = n_par_at_pool_ct[mask_i_p]
            nj_p        = int(np.sum(mask_i_p))
            sims_kont.append(cross_L_kontextual(
                ci_p, ci_p,
                n_par_p, n_par_p,
                n_pool_ct, nj_p, wi_p, RADII))

    sims_orig  = np.array(sims_orig)                        # (n_sim, N_R)
    stats_orig = _integral_stats(L_obs_orig, sims_orig, RADII)

    if sims_kont:
        sims_kont_arr  = np.array(sims_kont)
        stats_kont     = _integral_stats(L_obs_kont, sims_kont_arr, RADII)
    else:
        sims_kont_arr  = np.empty((0, N_R))
        stats_kont     = None

    results[(ct, ct)] = {
        "type_i":          ct,
        "type_j":          ct,
        "parent_name":     parent_name_ct,
        "perm_pool_types": sorted(perm_types_ct),
        "r":               RADII.copy(),
        # ---- original (global null) ----
        "L_original":              L_obs_orig,
        "L_original_sim_mean":     stats_orig['sim_mean'],
        "L_original_sim_std":      stats_orig['sim_std'],
        "L_original_simulations":  sims_orig,
        "pvalues_original":        stats_orig['pvalues'],
        "integral_original":       stats_orig['integral_signed'],
        "integral_original_abs":   stats_orig['integral_abs'],
        "z_original":              stats_orig['z_integral'],
        "p_original":              stats_orig['p_integral'],
        # ---- kontextual (pool-restricted null; NaN if pool = self-only) ----
        "L_kontextual":                L_obs_kont,
        "L_kontextual_sim_mean":       stats_kont['sim_mean']        if stats_kont else nan_r.copy(),
        "L_kontextual_sim_std":        stats_kont['sim_std']         if stats_kont else nan_r.copy(),
        "L_kontextual_simulations":    sims_kont_arr,
        "pvalues_kontextual":          stats_kont['pvalues']         if stats_kont else nan_r.copy(),
        "integral_kontextual":         stats_kont['integral_signed'] if stats_kont else np.nan,
        "integral_kontextual_abs":     stats_kont['integral_abs']    if stats_kont else np.nan,
        "z_kontextual":                stats_kont['z_integral']      if stats_kont else np.nan,
        "p_kontextual":                stats_kont['p_integral']      if stats_kont else np.nan,
        # ---- counts / diagnostics ----
        "n_source_cells":      n_ct,
        "n_target_cells":      n_ct,
        "n_valid_simulations": len(sims_orig),
        "n_failed_simulations": 0,
        "reason":              None,
    }
    print(f"  [{ct}] done  (n={n_ct}, pool types={sorted(perm_types_ct)})")

print(f"\nDiagonal done: "
      f"{sum(1 for (ti,tj),v in results.items() if ti==tj and v['reason'] is None)} "
      f"types computed.")

#==============================================================================
