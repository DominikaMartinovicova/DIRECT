    """NaN-filled result for skipped or failed pairs."""
    nan_r    = np.full(N_R, np.nan)
    empty_s  = np.empty((0, N_R))
    return {
        "type_i":                      type_i,
        "type_j":                      type_j,
        "parent_name":                 parent_name,
        "perm_pool_types":             [],
        "r":                           RADII.copy(),
        # original (homogeneous, global window)
        "L_original":                  nan_r.copy(),
        "L_original_sim_mean":         nan_r.copy(),
        "L_original_sim_std":          nan_r.copy(),
        "L_original_simulations":      empty_s.copy(),
        "pvalues_original":            nan_r.copy(),
        "integral_original":           np.nan,
        "integral_original_abs":       np.nan,
        "z_original":                  np.nan,
        "p_original":                  np.nan,
        # kontextual (inhomogeneous parent density, global window)
        "L_kontextual":                nan_r.copy(),
        "L_kontextual_sim_mean":       nan_r.copy(),
        "L_kontextual_sim_std":        nan_r.copy(),
        "L_kontextual_simulations":    empty_s.copy(),
        "pvalues_kontextual":          nan_r.copy(),
        "integral_kontextual":         np.nan,
        "integral_kontextual_abs":     np.nan,
        "z_kontextual":                np.nan,
        "p_kontextual":                np.nan,
        # counts / diagnostics
        "n_source_cells":              np.nan,
        "n_target_cells":              np.nan,
        "n_valid_simulations":         0,
        "n_failed_simulations":        0,
        "reason":                      reason,
    }


