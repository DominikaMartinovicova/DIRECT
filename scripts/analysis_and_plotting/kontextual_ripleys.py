# --- Global window (used for edge correction in ALL L computations) ----------
print("Building global observation window...")
_g_poly         = build_convex_hull_polygon(coordinates)
_g_hull_verts   = np.array(_g_poly.exterior.coords)[:-1]
global_area     = _g_poly.area
global_weights  = compute_edge_weights(coordinates, _g_hull_verts, RADII)
print(f"  Global area = {global_area:.0f} µm²  ({len(coordinates)} cells)\n")

# --- Per-pool cache -----------------------------------------------------------
# Key  : frozenset of pool cell-type strings
# Value: dict with coords, labels, indices, n_parent_at_pool, pool_global_weights
_pool_cache = {}


def get_pool(perm_types_iter):
    """
    Return cached pool data for the given set of cell types.
    Computes parent-count matrix on first access.
    Returns None if the pool has fewer than 4 cells.

    Stored fields
    -------------
    coords              : (n_pool, 2)
    labels              : (n_pool,)
    indices             : (n_pool,)   global cell indices
    n_parent_at_pool    : (n_pool, N_R) count of parent cells within each
                          radius of each pool cell (positions invariant
                          across label permutations → computed once)
    pool_global_weights : (n_pool, N_R) global edge-correction weights
    """
    key = frozenset(perm_types_iter)
    if key in _pool_cache:
        return _pool_cache[key]

    mask        = np.isin(labels, list(key))
    pool_idx    = np.where(mask)[0]
    pool_coords = coordinates[mask]
    pool_labels = labels[mask]

    if len(pool_coords) < 4:
        print(f"  [WARN] Pool {sorted(key)} has only {len(pool_coords)} cells — skipping.")
        _pool_cache[key] = None
        return None

    # Precompute parent counts for every pool cell at each radius.
    # This is computed once and reused for observed + all null simulations.
    n_parent_at_pool = precompute_parent_counts(pool_coords, pool_coords, RADII)

    pool = {
        "types":               key,
        "coords":              pool_coords,
        "labels":              pool_labels,
        "indices":             pool_idx,
        "n_parent_at_pool":    n_parent_at_pool,
        "pool_global_weights": global_weights[pool_idx],   # (n_pool, N_R)
    }
    _pool_cache[key] = pool
    print(f"  [POOL] {len(key)} types, {len(pool_coords)} cells  "
          f"({sorted(key)[:3]}{'...' if len(key) > 3 else ''})")
    return pool


