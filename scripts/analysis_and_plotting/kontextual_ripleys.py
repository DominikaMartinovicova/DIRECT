def build_convex_hull_polygon(coords):
    """Return a shapely Polygon for the convex hull of coords (n, 2)."""
    hull = ConvexHull(coords)
    return Polygon(coords[hull.vertices])


def min_dist_to_hull_boundary(coords, hull_vertices):
    """
    Vectorised minimum distance from each point to a convex polygon boundary.

    Parameters
    ----------
    coords        : (n_pts, 2) float array
    hull_vertices : (n_verts, 2) float array — ordered convex hull vertices

    Returns
    -------
    dists : (n_pts,) minimum distance from each point to the boundary
    """
    verts = np.asarray(hull_vertices, dtype=float)
    p0 = verts
    p1 = np.roll(verts, -1, axis=0)
    edge_vec    = p1 - p0
    edge_len_sq = np.sum(edge_vec ** 2, axis=1) + 1e-12

    pts = coords[:, np.newaxis, :]
    P0  = p0[np.newaxis, :, :]
    EV  = edge_vec[np.newaxis, :, :]

    t = np.sum((pts - P0) * EV, axis=2) / edge_len_sq
    t = np.clip(t, 0.0, 1.0)

    closest   = P0 + t[:, :, np.newaxis] * EV
    seg_dists = np.linalg.norm(pts - closest, axis=2)

    return seg_dists.min(axis=1)


def compute_edge_weights(coords, hull_vertices, radii, max_weight=3.0):
    """
    Isotropic edge correction weights (Ripley 1988).

    weight_i(r) = 1                       if dist_to_boundary(i) >= r
    weight_i(r) = min(r / dist_i, max_w)  otherwise

    Returns
    -------
    weights : (n_cells, n_radii)
    """
    dists   = min_dist_to_hull_boundary(coords, hull_vertices)
    weights = np.ones((len(coords), len(radii)), dtype=float)
    for k, r in enumerate(radii):
        near = dists < r
        if np.any(near):
            weights[near, k] = np.minimum(
                r / np.maximum(dists[near], 1e-6), max_weight
            )
    return weights


def precompute_parent_counts(query_coords, parent_coords, radii):
    """
    For each query cell, count parent cells within each radius.

    Self-pairs are included (a pool cell counts itself), matching R Statial's
    closepairs(..., distinct=FALSE) behaviour.

    Returns
    -------
    counts : (n_query, n_radii)  float32 array
    """
    tree   = cKDTree(parent_coords)
    nbs    = tree.query_ball_point(query_coords, radii[-1])
    counts = np.zeros((len(query_coords), len(radii)), dtype=np.float32)
    for i, nb_list in enumerate(nbs):
        if not nb_list:
            continue
        d = np.linalg.norm(parent_coords[np.array(nb_list)] - query_coords[i], axis=1)
        counts[i] = np.searchsorted(np.sort(d), radii, side='right')
    return counts


def cross_L_original(coords_i, coords_j, radii, weights_i, global_area):
    """
    Standard homogeneous cross-type Ripley's L  (R Statial 'original').

    Matches R's .Lfunction():
        K(r) = area / (n_i * n_j) * sum_i[ edge_i(r) * count_j(r|i) ]
        L(r) = sqrt(K / pi)   [un-centered; plotting subtracts r]

    Parameters
    ----------
    coords_i    : (n_i, 2)    source cell coordinates
    coords_j    : (n_j, 2)    target cell coordinates
    radii       : (n_r,)      evaluation radii
    weights_i   : (n_i, n_r)  pre-computed edge correction weights (global hull)
    global_area : float       area of the global convex hull window

    Returns
    -------
    L : (n_r,) un-centered Ripley's L
    """
    n_i, n_j = len(coords_i), len(coords_j)
    if n_i == 0 or n_j == 0:
        return np.full(len(radii), np.nan)

    lambda_j = n_j / global_area
    tree     = cKDTree(coords_j)
    nbs      = tree.query_ball_point(coords_i, radii[-1])

    weighted_sum = np.zeros(len(radii))
    for i, nb_list in enumerate(nbs):
        if not nb_list:
            continue
        d      = np.sort(np.linalg.norm(coords_j[np.array(nb_list)] - coords_i[i], axis=1))
        counts = np.searchsorted(d, radii, side='right')          # (n_r,)
        weighted_sum += counts * weights_i[i]                     # edge-weighted

    K = weighted_sum / lambda_j / n_i                             # (n_r,)
    return np.sqrt(np.maximum(K, 0.0) / np.pi)


def cross_L_kontextual(coords_i, coords_j,
                        n_parent_at_source, n_parent_at_target,
                        n_pool, n_j, weights_i, radii):
    """
    Inhomogeneous kontextual cross-type Ripley's L  (R Statial '.Kontext()').

    Uses local parent-cell density as the intensity function for both source
    and target cells, following Canete et al. 2022.

    Formula (per radius r):
        n_p_i(r) = count(parent cells within r of source i)
        n_p_j(r) = count(parent cells within r of target j)

        numerator(r)   = sum_{pairs (i,j) within r}
                            [ edge_i(r) * n_p_i * n_pool / (n_p_j * n_j) ]
        denominator(r) = sum_{source i} n_p_i(r)

        K(r) = pi * r^2 * numerator(r) / denominator(r)
        L(r) = sqrt(K / pi)   [un-centered]

    For uniform parent density this reduces to standard K / L.
    Pairs where n_p_j = 0 are skipped (undefined local density at target).
    Source cells with n_p_i = 0 contribute 0 to both numerator and denominator.

    Parameters
    ----------
    coords_i            : (n_i, 2)    source cell coordinates
    coords_j            : (n_j, 2)    target cell coordinates
    n_parent_at_source  : (n_i, n_r)  parent counts at source positions
    n_parent_at_target  : (n_j, n_r)  parent counts at target positions
    n_pool              : int         total number of parent (pool) cells
    n_j                 : int         total number of target cells
    weights_i           : (n_i, n_r)  edge correction weights (global hull)
    radii               : (n_r,)      evaluation radii

    Returns
    -------
    L : (n_r,) un-centered kontextual Ripley's L
    """
    if len(coords_i) == 0 or len(coords_j) == 0:
        return np.full(len(radii), np.nan)

    N_R  = len(radii)
    tree = cKDTree(coords_j)
    nbs  = tree.query_ball_point(coords_i, radii[-1])

    numerator   = np.zeros(N_R)
    denominator = np.zeros(N_R)

    for i, nb_list in enumerate(nbs):
        n_pi = n_parent_at_source[i]          # (N_R,)
        denominator += n_pi

        if not nb_list:
            continue

        nb_arr   = np.array(nb_list)
        d        = np.linalg.norm(coords_j[nb_arr] - coords_i[i], axis=1)
        sort_idx = np.argsort(d)
        d_sorted = d[sort_idx]
        nb_sorted = nb_arr[sort_idx]

        # Pre-fetch parent counts at all target neighbors: (n_nb, N_R)
        n_pj_all = n_parent_at_target[nb_sorted]

        for k, r in enumerate(radii):
            n_pi_k = n_pi[k]
            if n_pi_k == 0:
                continue

            n_in_r = int(np.searchsorted(d_sorted, r, side='right'))
            if n_in_r == 0:
                continue

            n_pj_k = n_pj_all[:n_in_r, k]
            valid   = n_pj_k > 0
            if not valid.any():
                continue

            # weight(i,j) = edge_i * n_p_i * n_pool / (n_p_j * n_j)
            numerator[k] += (weights_i[i, k] * n_pi_k
                             * np.sum(1.0 / n_pj_k[valid])
                             * n_pool / n_j)

    K = np.zeros(N_R)
    pos = (denominator > 0) & (numerator > 0)
    K[pos] = np.pi * radii[pos]**2 * numerator[pos] / denominator[pos]
    return np.sqrt(np.maximum(K, 0.0) / np.pi)


def _integral_stats(observed, sim_matrix, radii):
    """
    Compute signed integral and z-score statistics from observed L and sims.

    Parameters
    ----------
    observed   : (N_R,)
    sim_matrix : (n_valid, N_R)
    radii      : (N_R,)

    Returns
    -------
    dict with integral_signed, integral_abs, z_integral, p_integral,
    pvalues (per-radius), sim_mean, sim_std
    """
    sim_mean = np.nanmean(sim_matrix, axis=0)
    sim_std  = np.nanstd(sim_matrix,  axis=0)

    diff             = observed - sim_mean
    integral_signed  = float(np.trapezoid(diff,           radii))
    integral_abs     = float(np.trapezoid(np.abs(diff),   radii))

    obs_int_raw  = float(np.trapezoid(observed,  radii))
    sim_integrals = np.trapezoid(sim_matrix, radii, axis=1)

    int_std = sim_integrals.std()
    if int_std > 0:
        z_integral  = float((obs_int_raw - sim_integrals.mean()) / int_std)
        p_upper     = (np.sum(sim_integrals >= obs_int_raw) + 1) / (len(sim_integrals) + 1)
        p_integral  = float(np.minimum(p_upper, 1.0 - p_upper))
    else:
        z_integral = np.nan
        p_integral = np.nan

    n_valid = sim_matrix.shape[0]
    p_upper_per_r = (np.sum(sim_matrix >= observed[np.newaxis, :], axis=0) + 1) / (n_valid + 1)
    pvalues       = np.minimum(p_upper_per_r, 1.0 - p_upper_per_r)

    return dict(
        sim_mean        = sim_mean,
        sim_std         = sim_std,
        integral_signed = integral_signed,
        integral_abs    = integral_abs,
        z_integral      = z_integral,
        p_integral      = p_integral,
        pvalues         = pvalues,
    )


