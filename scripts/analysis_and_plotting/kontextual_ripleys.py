# kontextual_ripleys.py
#+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
#
#   Two parallel cross-type Ripley's L statistics matching R Statial output:
#
#   L_original   (= R "original"):
#     Standard homogeneous cross-type L using the global observation window.
#     K(r) = area/(n_i*n_j) * sum_i[ edge_i(r) * count_j(r|i) ]
#     Null: global label permutation across all cells, same global window.
#
#   L_kontextual (= R "kontextual", Canete et al. 2022):
#     Inhomogeneous L where intensity is estimated from local parent-cell
#     density rather than a global area-based constant.
#     K(r) = (n_pool/n_j)*pi*r^2 * sum_pairs[edge_i*n_p_i/n_p_j]
#                                 / sum_sources[n_p_i]
#     where n_p_x(r) = count of parent cells within r of cell x.
#     Null: within-parent label permutation, same global window + cached
#           parent counts (parent cell positions are permutation-invariant).
#
#   Both statistics share the same permutation draws and cached parent counts,
#   so the cost overhead of the second statistic is modest.
#
#   Global window (convex hull of all cells) is used for edge correction in
#   both statistics, matching R Statial's makeWindow("convex") behaviour.
#   No per-pool sub-window is constructed.
#
#   0  Import libraries and parse arguments
#   1  Read data
#   2  Define analysis functions
#   3  Global window + per-pool cache (parent counts)
#   4  Main analysis loop
#   5  Build summary table and save results
#
#
# Usage:
#   python3 /net/beegfs/groups/tgac/dmartinovicova_new/DIRECT/scripts/analysis_and_plotting/kontextual_ripleys.py \
